package cargo

import (
	"encoding/json"
	"fmt"
	"net/http"

	"github.com/datadrivers/go-nexus-client/nexus3/pkg/client"
	"github.com/datadrivers/go-nexus-client/nexus3/pkg/repository/common"
	"github.com/datadrivers/go-nexus-client/nexus3/pkg/tools"
	"github.com/datadrivers/go-nexus-client/nexus3/schema/repository"
)

const (
	cargoGroupAPIEndpoint = cargoAPIEndpoint + "/group"
)

type RepositoryCargoGroupService struct {
	client *client.Client
}

func NewRepositoryCargoGroupService(c *client.Client) *RepositoryCargoGroupService {
	return &RepositoryCargoGroupService{
		client: c,
	}
}

func (s *RepositoryCargoGroupService) Create(repo repository.CargoGroupRepository) error {
	data, err := tools.JsonMarshalInterfaceToIOReader(repo)
	if err != nil {
		return err
	}
	body, resp, err := s.client.Post(cargoGroupAPIEndpoint, data)
	if err != nil {
		return err
	}
	if resp.StatusCode != http.StatusOK && resp.StatusCode != http.StatusCreated {
		return fmt.Errorf("could not create repository '%s': HTTP: %d, %s", repo.Name, resp.StatusCode, string(body))
	}
	return nil
}

func (s *RepositoryCargoGroupService) Get(id string) (*repository.CargoGroupRepository, error) {
	var repo repository.CargoGroupRepository
	body, resp, err := s.client.Get(fmt.Sprintf("%s/%s", cargoGroupAPIEndpoint, id), nil)
	if err != nil {
		return nil, err
	}
	if resp.StatusCode != http.StatusOK {
		return nil, fmt.Errorf("could not read repository '%s': HTTP: %d, %s", id, resp.StatusCode, string(body))
	}
	if err := json.Unmarshal(body, &repo); err != nil {
		return nil, fmt.Errorf("could not unmarshal repository: %v", err)
	}
	return &repo, nil
}

func (s *RepositoryCargoGroupService) Update(id string, repo repository.CargoGroupRepository) error {
	data, err := tools.JsonMarshalInterfaceToIOReader(repo)
	if err != nil {
		return err
	}
	body, resp, err := s.client.Put(fmt.Sprintf("%s/%s", cargoGroupAPIEndpoint, id), data)
	if err != nil {
		return err
	}
	if resp.StatusCode != http.StatusNoContent {
		return fmt.Errorf("could not update repository '%s': HTTP: %d, %s", id, resp.StatusCode, string(body))
	}
	return nil
}

func (s *RepositoryCargoGroupService) Delete(id string) error {
	return common.DeleteRepository(s.client, id)
}
